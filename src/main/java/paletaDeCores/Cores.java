package paletaDeCores;

import java.awt.*;

public class Cores {
    /* S = Strong
     * L = Light*/
    static Color blackS = new Color(28, 27, 27);
    static Color blackL = new Color(53, 53, 53);
    static Color purpleS = new Color(71, 51, 196);
    static Color orangeS = new Color(255, 124, 67);

    public static Color getBlackS() {
        return blackS;
    }

    public void setBlackS(Color blackS) {
        this.blackS = blackS;
    }

    public static Color getBlackL() {
        return blackL;
    }

    public void setBlackL(Color blackL) {
        this.blackL = blackL;
    }

    public static Color getPurpleS() {
        return purpleS;
    }

    public void setPurpleS(Color purpleS) {
        this.purpleS = purpleS;
    }

    public static Color getOrangeS() {
        return orangeS;
    }

    public void setOrangeS(Color orangeS) {
        this.orangeS = orangeS;
    }


}
